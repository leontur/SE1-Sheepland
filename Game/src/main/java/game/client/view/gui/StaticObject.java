package game.client.view.gui;

import javax.swing.JLabel;

/**
 * FATHER CLASS OF A GENERIC STATIC OBJECT
 * 
 * PROPERTIES
 * 	-view
 * 
 * @author Leonardo
 *
 */
public class StaticObject extends JLabel {

	/**
	 * CLASS SERIAL VERSION ID
	 */
	private static final long serialVersionUID = 1L;
	
	/**
	 * CLASS VARS
	 */
	private boolean isVisibleObject;
	protected String dirPath = "./resources/img/";
	protected String dirPathCards = "./resources/img/cards/";
	protected String dirPathGif = "./resources/gif/";
	private boolean isActiveObject;
	
	/**
	 * CLASS CONTSTRUCTOR
	 */
	public StaticObject(){
		isVisibleObject = true;
		isActiveObject = true;
	}
	
	/**
	 * CLASS METHODS
	 */
	
	/**
	 * True if the object is visible 
	 * or false otherwise
	 * @return
	 */
	public boolean isVisibleObject(){
		return this.isVisibleObject;
	}
	
	/**
	 * Set true if the object is visible 
	 * or set false otherwise 
	 * @param status
	 */
	public void setIsVisibleObject(boolean status){
		this.isVisibleObject = status;
	}

	/**
	 * True if the object is active
	 * or false otherwise
	 * @return
	 */
	public boolean isActiveObject(){
		return this.isActiveObject;
	}
	
	/**
	 * Set true if the object is active
	 * or set false otherwise 
	 * @param status
	 */
	public void setIsActiveObject(boolean status){
		this.isActiveObject = status;
	}
	
}